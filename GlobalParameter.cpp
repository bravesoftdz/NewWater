{
	unsigned char CRC16Hi=0xff,CRC16Lo=0xff;
	int  i;
	unsigned char iIndex;
	for (i=0;i<len;i++)
	{
		iIndex = CRC16Lo ^ InData[i];
		CRC16Lo = CRC16Hi ^ CRCLo_arry[iIndex] ;
		CRC16Hi = CRCHi_arry[iIndex];
	}
	CRCData[0]=CRC16Hi;
	CRCData[1]=CRC16Lo;
}

